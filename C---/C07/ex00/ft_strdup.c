#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		length;
	int		i;

	i = 0;
	length = 0;
	while (src[length])
		length++;
	str = malloc(sizeof(char) * (length + 1));
	if (!str)
		return (0);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
